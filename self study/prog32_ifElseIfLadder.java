public class prog32_ifElseIfLadder {

    public static void main(String arg[]) {
        int num = 10;

        if (num > 0) {
            System.out.println(num + " is a +ve number.");
        } else if (num == 0) {
            System.out.println(num + " is a zero");
        } else {
            System.out.println(num + " is a -ve number");
        }
    }
}
