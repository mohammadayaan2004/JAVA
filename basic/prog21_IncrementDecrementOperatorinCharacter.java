public class prog21_IncrementDecrementOperatorinCharacter {
    public static void main(String[] args) {
        char ch1 = 'D';
        char ch2 = 'D';
        char ch3 = 'D';
        char ch4 = 'D';
        System.out.println("Pre increment operator(++ch1) :- " + (++ch1));
        System.out.println("Post increment operator(ch2++) :- " + (ch2++));
        System.out.println("Pre Decrement operator(--ch3) :- "+(--ch3));
        System.out.println("Post Decrement operator(ch4--) :- "+(ch4--));
    }    
}
